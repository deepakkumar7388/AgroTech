val LocalAppStrings = staticCompositionLocalOf { EnglishStrings }

@Composable
fun ProvideAppStrings(
    languageCode: String,
    content: @Composable () -> Unit
) {
    val strings = when (languageCode) {
        "hi" -> HindiStrings
        "mr" -> MarathiStrings
        "pa" -> PunjabiStrings
        "gu" -> GujaratiStrings
        "bn" -> BengaliStrings
        "te" -> TeluguStrings
        "ta" -> TamilStrings
        else -> EnglishStrings
    }
    CompositionLocalProvider(LocalAppStrings provides strings, content = content)
}
